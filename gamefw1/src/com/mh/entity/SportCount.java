package com.mh.entity;

public class SportCount {
	private String timeType;
	private String ftRtype;
	private String bkRtype;
	private int ftCount;
	private int bkCount;

	public String getTimeType() {
		return timeType;
	}

	public void setTimeType(String timeType) {
		this.timeType = timeType;
	}

	public String getFtRtype() {
		return ftRtype;
	}

	public void setFtRtype(String ftRtype) {
		this.ftRtype = ftRtype;
	}

	public String getBkRtype() {
		return bkRtype;
	}

	public void setBkRtype(String bkRtype) {
		this.bkRtype = bkRtype;
	}

	public int getFtCount() {
		return ftCount;
	}

	public void setFtCount(int ftCount) {
		this.ftCount = ftCount;
	}

	public int getBkCount() {
		return bkCount;
	}

	public void setBkCount(int bkCount) {
		this.bkCount = bkCount;
	}

}
